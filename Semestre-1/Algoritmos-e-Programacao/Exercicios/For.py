# for <elemento> in <sequencia>:
#   <instrucao1>

# versão mais simples
for letra in "texto":
    print(letra)

# versão de range
for numero in range(0, 20, 2):
    print(numero)

# versão de range com passo negativo
for numero in range(20, 0, -2):
    print(numero)

# versão de range acrescentando 1
for numero in range(0, 20):
    print(numero + 1)

# versão de range com passo negativo acrescentando 1
for numero in range(20, 0, -1):
    print(numero - 1)

# versão de range descrescente
for numero in range(20, 0, -1):
    print(numero)
